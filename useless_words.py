def words():
    words = set()

    # Words
    words.add("is")
    words.add("the")
    words.add("are")
    words.add("am")
    words.add("a")
    words.add("it")
    words.add("was")
    words.add("were")
    words.add("an")

    # Punctuation
    words.add(",")
    words.add(".")
    words.add("?")

    return words